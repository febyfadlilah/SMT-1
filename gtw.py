#Buatlah program jika andi memasukkan nilai 1 sampai 9,maka outputnya "angka anda Satu"-"angka anda Sembilan"
#Gunakan operasi if,elif dan else

angka=int(input('Masukkan Angka: '))
if angka == 1 :
    print('Angka anda Satu')
elif angka == 2 :
    print('Angka anda Dua')
elif angka == 3 :
    print('Angka anda Tiga')
elif  angka == 4 :
    print('Angka anda Empat')
elif angka == 5 :
    print('Angka anda Lima')
elif angka == 6 :
    print('Angka anda Enam')
elif angka == 7 :
    print('Angka anda Tujuh')
elif angka == 8 :
    print('Angka anda Delapan')
elif angka == 9 :
    print('Angka anda Sembilan')
else :
    print('Maaf, angka yang anda masukkan tidak terdeteksi')
    print('Coba Lagi dengan angka lainnya')
    print('Terima Kasih')